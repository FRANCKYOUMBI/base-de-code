function getRandomDatetime(): Date {
    var startDate = new Date('2023-05-28');
    var endDate = new Date('2023-7-5')
    var startTime = startDate.getTime();
    var endTime = endDate.getTime();
    var randomTime = Math.random() * (endTime - startTime) + startTime;
    var randomDate = new Date(randomTime);
    return randomDate;
}


